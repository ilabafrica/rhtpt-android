public class Option {
    int id;
    String title;

    // constructors
    public Option() {
    }
    // setters
    public void setId(int id) {
        this.id = id;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    // getters
    public int getId() {
        return this.id;
    }

    public String setTitle() {
        return this.title;
    }
}